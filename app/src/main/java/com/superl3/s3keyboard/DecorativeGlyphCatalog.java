    static String glyphSourceType(String glyphId) {
        if (isBuiltInFontGlyph(glyphId)) {
            return SOURCE_FONT;
        }
        if (isBuiltInImageMaskGlyph(glyphId)) {
            return SOURCE_IMAGE_MASK;
        }
        if (isBuiltInPointGlyph(glyphId)
                || isBuiltInKeyboardGlyph(glyphId)
                || isBuiltInGmkStyleGlyph(glyphId)) {
            return SOURCE_VECTOR;
        }
        return "";
    }

