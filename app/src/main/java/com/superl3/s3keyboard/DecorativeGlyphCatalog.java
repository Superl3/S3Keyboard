    static float dotRadiusForKeyHeight(float keyHeight, float minRadius) {
        return Math.max(minRadius, keyHeight * DOT_DIAMETER_TO_KEY_HEIGHT / 2f);
    }

    static float lineWeightForDotRadius(float radius) {
        return radius / DOT_RADIUS_TO_LINE_WEIGHT;
    }

    static float spaceDotGap(float radius, float minGap) {
        return Math.max(radius * SPACE_DOT_GAP_TO_RADIUS, minGap);
    }

